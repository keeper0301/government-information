// ============================================================
// 정책명 title 정제 — 네이버 검색·클릭(CTR) 최적화 (2026-06-11)
// ============================================================
// 서치어드바이저 실측: CTR 낮은 페이지 title 에 기관명("(XX재단)")이 들어가 검색의도를
// 방해하고, 지역 정보가 뒤 괄호에 묻혀 클릭 유도가 약했음.
//   - 끝 괄호가 기관명(재단·공단·진흥원 등) → 제거 ("햇살론 플러스 (부산신용보증재단)" → "햇살론 플러스")
//   - 끝 괄호가 행정구역(시/군/구/도) → 가장 구체적 구역명을 앞으로
//     ("출산지원금 지급 (부산광역시 사하구)" → "사하구 출산지원금 지급")
// 정책 종류 괄호("(자녀양육비)" 처럼 중간) 는 끝 괄호가 아니라 건드리지 않음.
// ============================================================

// "센터" 는 정책명 핵심어(치매안심센터·주민센터)일 수 있어 제외 — 명확한 기관 접미사만.
const ORG_SUFFIX =
  /(재단|공단|공사|진흥원|연구원|연구소|협회|위원회|조합|금고|은행|보증|진흥회|관리단|진흥공단|복지공단)$/;
const REGION = /(특별시|광역시|특별자치시|특별자치도)/;
const REGION_SUFFIX = /[가-힣]+(시|군|구|도)$/;

export function cleanPolicyTitle(title: string): string {
  const t = (title ?? "").trim();
  const m = /\s*\(([^)]+)\)\s*$/.exec(t);
  if (!m) return t; // 끝 괄호 없음 — 원본 유지
  const inner = m[1].trim();
  const base = t.replace(/\s*\([^)]+\)\s*$/, "").trim();
  if (!base) return t; // 괄호가 제목 전부면 원본 유지 (안전)

  // ① 기관명 → 제거
  if (ORG_SUFFIX.test(inner)) return base;

  // ② 행정구역(지역) → 가장 구체적 구역명을 앞으로. base 가 이미 그 구역명으로 시작하면
  // 중복("강남구 청년지원 (강남구)" → "강남구 강남구 청년지원") 방지.
  if (REGION.test(inner) || REGION_SUFFIX.test(inner)) {
    const region = inner.split(/\s+/).pop() ?? "";
    if (!region || base.startsWith(region)) return base;
    return `${region} ${base}`;
  }

  // ③ 기관·지역이 아닌 끝 괄호(대상·연도·세부명 등)는 건드리지 않는다. 무조건 제거 시
  // 식별 정보 손실 + 동명 정책 title 중복(네이버 진단 악화) 위험 → 원본 유지가 안전.
  return t;
}
